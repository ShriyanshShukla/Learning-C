#include <stdio.h>

int main()
{
    int i = 5;
    printf("The value of ++i is %d\n", ++i);
    printf("The value of i++ is %d\n", i++);
    printf("The value of i is %d\n", i);
    // Decrement Operator works the same way

    int a = 10;
        a += 20;
    printf("The value of a+=20 is %d\n", a);
    // Decrement Operator works the same way

    return 0;
}
